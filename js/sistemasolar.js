//button appearance.
function undoClick(i){
    $("b").style.display = i;
    $("b").style.opacity = "1";
}

//main function for text replacement and restarting.
function replaceAndGo(next){
    word_counter = 0;
    main_text = next;
    $("allTexts").innerHTML = "";
    setTimeout(texts, 250);
    viewPlanet();
}

//function for planets images appearance.
function appear(x){
    for(let i = 0; i < document.getElementsByClassName("planets").length; i++){
        document.getElementsByClassName("planets")[i].style.transform = `translate(-50%, -50%) scale(0)`;
        document.getElementsByClassName("planets")[i].style.opacity = "0";
    }
    $(x).style.transform = `translate(-50%, -40%) scale(1)`;
    $(x).style.opacity = "1";
}

//function for appearing new text and image, dissapearance of click button.
function multiTexts(text, id){
    undoClick("none");
    replaceAndGo(text);
    appear(id);
}

//adding the next event to the body.
function events(i){
    document.body.addEventListener("click", i, {once:true});
}

